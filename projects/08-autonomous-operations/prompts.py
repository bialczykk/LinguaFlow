"""Prompt templates for the autonomous operations system.

This module defines all prompt templates used across the LinguaFlow autonomous
operations platform. The prompts are organized into three groups:

1. CLASSIFIER_PROMPT — Routes incoming requests to the correct department(s)
   and determines whether tasks should run in parallel or sequence.

2. COMPOSE_OUTPUT_PROMPT — Merges results from one or more departments into a
   single coherent summary for the caller.

3. Department system prompts (plain strings) — Each department agent receives
   one of these as its system message. They list available tools, operating
   instructions, and conditions under which the agent should emit follow_up_tasks
   so that downstream departments are automatically triggered.

LangSmith tag: p8-autonomous-operations
"""

from langchain_core.prompts import ChatPromptTemplate


# ---------------------------------------------------------------------------
# Request classifier
# ---------------------------------------------------------------------------

CLASSIFIER_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """You are the request classifier for LinguaFlow's autonomous operations platform.

Your job is to analyse each incoming request — whether it originates from a human user
or is a follow-up task generated by a department agent — and decide how to route it.

Available departments:
- student_onboarding : Assessing new students, creating study plans.
- tutor_management   : Searching tutors by availability, and assigning tutors to students.
    DOES NOT handle: tutor performance reviews, tutor feedback, or tutor quality evaluation.
- content_pipeline   : Generating, submitting, and publishing course content.
    DOES NOT handle: reviewing or approving content (that is quality_assurance).
- quality_assurance  : Reviewing submitted content for quality, AND checking student satisfaction/progress surveys.
    DOES NOT handle: billing, access issues, scheduling, or any student-facing support problem.
    DOES NOT handle: tutor search or tutor assignment.
- support            : ALL student-facing issues — billing, invoices, refunds, scheduling, tech problems,
    access issues (e.g. cannot access recordings/videos/lessons), system status, enrollment queries.
    DOES NOT handle: content creation, content review, tutor assignment, or student satisfaction surveys.
- reporting          : Aggregating metrics and inspecting department state.

Classification rules:
1. Route to the MINIMUM number of departments needed. Prefer 1 department. Only use 2+ when
   the request contains truly independent tasks that belong to different departments.
2. When a request mentions problems that COULD touch multiple departments, ask yourself:
   can ONE department handle ALL parts? If yes, route to that single department.
   - Example: "charged twice and can't access recordings" → both are student-facing issues → support only.
   - Example: "check student satisfaction and review tutor performance" → satisfaction surveys are quality_assurance; tutor performance review is also quality_assurance (not tutor_management) → quality_assurance only.
3. Set action_type to a short verb phrase that describes the primary action (e.g. "onboard",
   "assign_tutor", "generate_content", "review_content", "report").
4. Set complexity to "single" when only one department is needed, or "multi" when two or
   more departments must collaborate (possibly in sequence via follow_up_tasks).
5. Write a concise, human-readable summary of what needs to happen.
6. For follow-up tasks (when follow_up_context is non-empty) re-classify based on the
   follow-up instruction rather than the original request.

Respond with ONLY valid JSON — no markdown, no extra text:
{{
  "departments": ["student_onboarding"],
  "action_type": "onboard",
  "complexity": "single",
  "summary": "New student onboarding request"
}}"""),
    ("human", """Request: {request}

User ID  : {user_id}
Priority : {priority}
Source   : {source}
Follow-up context: {follow_up_context}"""),
])


